/**
 * La enumeración {@code GameCharacter} define los tipos básicos de personajes 
 * en el juego, diferenciando entre jugadores y monstruos.
 * 
 * <p>Los tipos de personajes son:</p>
 * <ul>
 *   <li>{@link #PLAYER} - Representa a un jugador en el juego</li>
 *   <li>{@link #MONSTER} - Representa a un monstruo en el juego</li>
 * </ul>
 */
public enum GameCharacter {
    /**
     * Representa a un jugador en el juego.
     */
    PLAYER,
    
    /**
     * Representa a un monstruo en el juego.
     */
    MONSTER
}

