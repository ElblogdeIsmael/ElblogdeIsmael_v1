/**
 * La enumeración {@code Orientation} define las orientaciones posibles para la colocación
 * de elementos dentro del laberinto en un sistema bidimensional.
 * 
 * <p>Las orientaciones disponibles son:</p>
 * <ul>
 *   <li>{@link #VERTICAL} - Colocación en orientación vertical</li>
 *   <li>{@link #HORIZONTAL} - Colocación en orientación horizontal</li>
 * </ul>
 * 
 * <p>Esta enumeración se utiliza para definir la orientación de estructuras, 
 * como bloques o caminos, en el laberinto.</p>
 */
public enum Orientation {
    
    /**
     * Representa la orientación vertical.
     */
    VERTICAL,
    
    /**
     * Representa la orientación horizontal.
     */
    HORIZONTAL
}
